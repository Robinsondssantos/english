  function getRandomInteger(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
  }

  function getWord(words, index) {
    return words.at(index);
  }

  function removeWord(words, word) {
    // wordsCopy = [...words];
    return words.filter(item => item.sentence !== word)
  }

